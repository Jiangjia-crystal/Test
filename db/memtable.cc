size_t MemTable::getEncodedLength(Iterator* iter) const { 
  Slice k = iter->key();
  Slice v = iter->value();
  size_t internal_key_size = k.size() + 8;
  size_t encoded_length = VarintLength(internal_key_size) +  
                          internal_key_size + VarintLength(v.size()) +
                          v.size();
  return encoded_length;
}

